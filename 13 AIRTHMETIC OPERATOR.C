// AIRTHMETIC OPEREATOR i.e, +,-,/,%,*

#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter Value Of A and B");
    scanf("%d %d",&a,&b);
    printf("Sum Of A and B is %d\n",a+b);
    printf("Difference Of A and B is %d\n",a-b);
    printf("Multiplication Of A and B is %d\n",a*b);
    printf("Division Of A and B is %d\n",a/b);   
    printf("Modulous Of A and B is %d",a%b);

    // (%) used to print reminder and (/) used to print quotient
    return 0;
}
